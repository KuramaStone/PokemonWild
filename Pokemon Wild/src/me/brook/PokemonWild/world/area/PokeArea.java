package me.brook.PokemonWild.world.area;

/*
 * This is a single area alone. For multiple routes joining, look at PokeWorld
 */
public class PokeArea {
	
	private int offsetX, offsetY;
	private int width;
	private int[] tiles;

	public PokeArea(int offsetX, int offsetY, int width, int[] tiles) {
		this.offsetX = offsetX;
		this.offsetY = offsetY;
		this.width = width;
		this.tiles = tiles;
	}

	public int getOffsetX() {
		return offsetX;
	}

	public int getOffsetY() {
		return offsetY;
	}

	public int getWidth() {
		return width;
	}

	public int[] getTiles() {
		return tiles;
	}

}
